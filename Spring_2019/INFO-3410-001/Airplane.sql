CREATE TABLE Airlines (
AirlineID INT PRIMARY KEY IDENTITY(1,1),
AirlineName VARCHAR(20)
);
GO

CREATE TABLE Planes (
PlaneID INT PRIMARY KEY IDENTITY(100,1),
Manufacturer VARCHAR(10),
ModelNum VARCHAR(20),
OriginalPurchaseDate DATE,
LastServiceDate DATE,
AirlineID INT REFERENCES Airlines(AirlineID),
NumSeats INT
);
GO

CREATE TABLE Airports (
AirportCode VARCHAR(3) PRIMARY KEY,
AirportLocation VARCHAR(25),
YearOpened INT,
NumTerminals INT
);
GO

CREATE TABLE Flights (
FlightID INT PRIMARY KEY IDENTITY(1000,1),
PlaneID INT REFERENCES Planes(PlaneID),
AirlineID INT REFERENCES Airlines(AirlineID),
AirportCodeOfDeparture VARCHAR(3) REFERENCES Airports(AirportCode),
AirportCodeOfArrival VARCHAR(3) REFERENCES Airports(AirportCode),
DepartureDateTime DATETIME,
ArrivalDateTime DATETIME
);
GO

